/// <summary>
///		The different types of records that can be shown in the inspector.
/// </summary>
public enum InspectorRecordType
{
	/// <summary>
	///		A collapsable group of records.
	/// </summary>
	Foldout,

	/// <summary>
	///		A C# property.
	/// </summary>
	Property,

	/// <summary>
	///		A C# field.
	/// </summary>
	Field
}